const os = require("os");

// Get the platform
console.log(os.platform());

// Get CPU architecture
console.log(os.arch());

// Get CPU core info
console.log(os.cpus());

// Get Free Memory
console.log(os.freemem());

// Get total memory
console.log(os.totalmem());

//Home dir
console.log(os.homedir());

// Uptime
console.log(os.uptime());

